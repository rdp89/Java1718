/**
* <h1>2. Clase  AreaRectangulo. </h1>
* <p>La fórmula del área de un rectángulo es b * h, siendo b la base y h la altura. </p>
* <p> Calcula y muestra el área de un rectángulo, siendo b=2 y h=7. </p>
* @author Rafael Delgado Peña
* @version 1.0
*/
public class AreaRectangulo{
	public static void main(String[] args) {
		byte b = 2;//Base
		byte h = 7;//Altura
		int area = b * h;//Formula del area
		System.out.println("El area del rectangulo con base "+b+" y altura "+h+" es "+area);
	}
}